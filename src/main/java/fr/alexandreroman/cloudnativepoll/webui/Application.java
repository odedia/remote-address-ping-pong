package fr.alexandreroman.cloudnativepoll.webui;

import java.io.IOException;
import java.net.InetSocketAddress;
import java.net.Socket;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RestController;

import lombok.extern.slf4j.Slf4j;

@SpringBootApplication
public class Application {
    public static void main(String[] args) {
        SpringApplication.run(Application.class, args);
    }
}

@RestController
@Slf4j
class VotesController {
    @GetMapping("/ping/{ip}/{port}")
    String getPing(@PathVariable String ip, @PathVariable Integer port) {
    	log.info("Got IP {} and port {}", ip, port);
    	return pingHost(ip, port, 5000) ? "Yay, I could connect!" : "Bummer, I couldn't connect after 5 seconds.";    			
    }
    
    public static boolean pingHost(String host, int port, int timeout) {
        try (Socket socket = new Socket()) {
            socket.connect(new InetSocketAddress(host, port), timeout);
            return true;
        } catch (IOException e) {
            return false; 
        }
    }

    
}
